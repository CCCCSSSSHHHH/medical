package medical;

public class Medical {
	
	private int medicalID;
	private String medicalTitle;
	private String userID;
	private String medicalDate;
	private String medicalContent;
	private int medicalAvailable;
	private int medicalhit;
	
	public int getMedicalID() {
		return medicalID;
	}
	public void setMedicalID(int medicalID) {
		this.medicalID = medicalID;
	}
	public String getMedicalTitle() {
		return medicalTitle;
	}
	public void setMedicalTitle(String medicalTitle) {
		this.medicalTitle = medicalTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getMedicalDate() {  
		return medicalDate;
	}
	public void setMedicalDate(String medicalDate) {
		this.medicalDate = medicalDate;
	}
	public String getMedicalContent() {
		return medicalContent;
	}
	public void setMedicalContent(String medicalContent) {
		this.medicalContent = medicalContent;
	}
	public int getMedicalAvailable() {
		return medicalAvailable;
	}
	public void setMedicalAvailable(int medicalAvailable) {
		this.medicalAvailable = medicalAvailable;
	}
	public int getMedicalhit() {
		return medicalhit;
	}
	public void setMedicalhit(int medicalhit) {
		
		this.medicalhit = medicalhit;
	}
	
}
